#define _CRT_SECURE_NO_WARNINGS 1

#include <stdio.h>

int main()
{
	char a = 3;
	//000000000000000000000000000000000011
	//000000011 - a

	char b = 127;
	//000000000000000000000000000001111111
	//01111111 - b

	//a和b相加
	//000000000000000000000000000000000011     //整型提升  --> 计算过程中表达式里的操作数大小达不到一个整型大小的时候
	//000000000000000000000000000001111111     //整型提升  --> 会发生整型提升，变成一个整型再来计算
	//000000000000000000000000000010000010

	char c = a + b;
	//10000010 - c
	//有符号数高位填充变成32比特位
	//111111111111111111111111111110000010  -补码
	//111111111111111111111111111110000001  -反码
	//100000000000000000000000000001111110  -原码
	//-126
	printf("%d\n", c);;
	return 0;
}
