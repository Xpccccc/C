#define _CRT_SECURE_NO_WARNINGS 1

#include <stdio.h>

int i;//全局变量，不初始化，默认为0

int main()
{
	i--;  //i = -1  
	//-1
	//10000000000000000000000000000000
	//11111111111111111111111111111110
	//11111111111111111111111111111111
	//此时i也变成无符号数
	//第一位不再是符号位，变成有效位
	//11111111111111111111111111111111
	if (i > sizeof(i))  //sizeof() - 计算变量/类型所占空间的大小 >=0  无符号数   
	{
		printf(">\n");
	}
	else
	{
		printf("<\n");
	}

	return 0;
}